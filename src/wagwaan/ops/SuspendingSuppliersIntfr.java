    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(!jCheckBox1.isSelected()){
            JOptionPane.showMessageDialog(this, "You must select the checkbox above to suspend the selected supplier", "Caution!!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if(jTextField6.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "You must specify the suspension reason", "Information!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "You must select the supplier ID", "Information!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "You must select the supplier Names", "Information!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
            PreparedStatement pr=null;
            String sql="update stock_suppliers set active=false where supplier_id='"+jTextField1.getText()+"' and supplier_names='"+jTextField2.getText()+"'";
            try {
                con.setAutoCommit(false);
                pr=con.prepareStatement(sql);
                pr.executeUpdate();
                
                String sqls="INSERT INTO supplier_suspensions(suspension_reason, supplier_id)VALUES (?, ?)";
                pr=con.prepareStatement(sqls);
                pr.setString(1, jTextField6.getText());
                pr.setString(2, jTextField1.getText());
                pr.executeUpdate();
                
                if(pr!=null){
                    JOptionPane.showMessageDialog(this, "Update is successful", "Information!!", JOptionPane.PLAIN_MESSAGE);
                    resetDetails();
                    con.commit();
                    con.setAutoCommit(true);
                }
            } catch (SQLException ex) {
                Logger.getLogger(SuspendingSuppliersIntfr.class.getName()).log(Level.SEVERE, null, ex);
                try {
                    con.rollback();
                } catch (SQLException ex1) {
                    Logger.getLogger(SuspendingSuppliersIntfr.class.getName()).log(Level.SEVERE, null, ex1);
                }
            }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

