    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jComboBox1.getSelectedItem().equals("STD. 8")){
        JOptionPane.showMessageDialog(this, "You cannot promote student already in STD. 8");
        jButton2.setEnabled(false);
        return;
        }
        if(jComboBox1.getSelectedItem().equals(jComboBox2.getSelectedItem())){
        JOptionPane.showMessageDialog(this, "You cannot promote a student to his/her previous class");
        return;
        }
        PreparedStatement pr=null;
        try{
            con.setAutoCommit(false);
        for(int i=0;i<jXTable1.getRowCount();i++){
            if(jXTable1.getModel().getValueAt(i, 0)!=null && jXTable1.getModel().getValueAt(i, 2)==Boolean.TRUE){
                String sql="update student_Registration set students_class='"+jComboBox2.getSelectedItem()+"' where student_id='"+jXTable1.getValueAt(i, 0)+"'";
                
                    con.setAutoCommit(false);
                    pr=con.prepareStatement(sql);
                    pr.executeUpdate();
            }
            }
        if(pr!=null){
        JOptionPane.showMessageDialog(this, "Insert Successful");
        resetDetails();
        }
            
        con.commit();
        con.setAutoCommit(true);
        } catch (SQLException ex) {
        Logger.getLogger(StudentsPromIntfr.class.getName()).log(Level.SEVERE, null, ex);
    }
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed


