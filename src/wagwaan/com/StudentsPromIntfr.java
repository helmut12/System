    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox1ActionPerformed
        String sql="select student_id, first_name||' '||middle_name||' '||last_name as names from student_Registration where students_class='"+jComboBox1.getSelectedItem()+"'";
    try {
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery(sql);
        int i=0;
        while(rs.next()){
        jXTable1.getModel().setValueAt(rs.getObject(1), i, 0);
        jXTable1.getModel().setValueAt(rs.getObject(2), i, 1);
        i++;
        }
    }
    catch(SQLException exc){
    exc.printStackTrace();
    }
    }//GEN-LAST:event_jCheckBox1ActionPerformed

