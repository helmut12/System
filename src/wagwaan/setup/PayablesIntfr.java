    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PreparedStatement pr=null;
        String sql="insert into payables(payable_item)values(?)";
        String insert=null;
        int count=0, j=0;
        try{
            con.setAutoCommit(false);
            for(j=0;j<jTable1.getRowCount();j++){
                insert=String.valueOf(jTable1.getValueAt(j, 0));
                
            ResultSet rs=SQLHelper.getResultset(con, "Select count(payable_item) from payables where payable_item='"+insert+"'");
            while(rs.next()){
                count=rs.getInt(1);
            }
            System.out.println("count is "+count);
            if(count>0){
            JOptionPane.showMessageDialog(this, "Item '"+insert+"' exists in the database");
            jButton1.setEnabled(false);
            return;
            }
            else if(count==0){
            jButton1.setEnabled(true);
            if(jTable1.getValueAt(j, 0)!=null){
            
        
                    pr=con.prepareStatement(sql);
                    pr.setObject(1, jTable1.getValueAt(j, 0));
                    pr.executeUpdate();
            }
        
        }
            
            }
            
        con.commit();
        con.setAutoCommit(true);
        if(pr!=null){
        JOptionPane.showMessageDialog(this, "Insert is Successful");
        resetDetails();
        }
            
            } catch (SQLException ex) {
                    Logger.getLogger(PayablesIntfr.class.getName()).log(Level.SEVERE, null, ex);
            try {
                con.rollback();
            } catch (SQLException ex1) {
                Logger.getLogger(PayablesIntfr.class.getName()).log(Level.SEVERE, null, ex1);
            }
                    
                }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
                int rows2Delete = jTable1.getSelectedRowCount();
        
        int[] selectedRows = jTable1.getSelectedRows();
        
        if (rows2Delete < 1) {
            
            java.awt.Toolkit.getDefaultToolkit().beep();
            
            javax.swing.JOptionPane.showMessageDialog(this, "There are no selected rows to delete!");
            
        }
        else if (rows2Delete > 1) {
                
                for (int i = 0; i < selectedRows.length; i++) {
                    
                    javax.swing.table.DefaultTableModel defTableModel = (javax.swing.table.DefaultTableModel)jTable1.getModel();
                    
                    defTableModel.removeRow(selectedRows[i]);
                    
                }
                
            }
        else {
                
                javax.swing.table.DefaultTableModel defTableModel = (javax.swing.table.DefaultTableModel)jTable1.getModel();
                
                defTableModel.removeRow(jTable1.getSelectedRow());
            }
    }//GEN-LAST:event_jButton4ActionPerformed


